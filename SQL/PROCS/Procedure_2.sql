CREATE PROCEDURE buscaNotasClientes
@CPF AS VARCHAR(12) = '19290992743',
@DATA_INICIAL AS DATETIME = '20160101',
@DATA_FINAL AS DATETIME = '20161231'

AS

BEGIN 

	SELECT *
	FROM [NOTAS FISCAIS] 
	WHERE CPF = @CPF
		AND DATA BETWEEN @DATA_INICIAL AND @DATA_FINAL

END

EXEC buscaNotasClientes '19290992743', '20160501'
